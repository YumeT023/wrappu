/**
 * enum which contains all existing error code
 */
export enum Code {
  /**
   * unknown `arg`
   */
  C200 = "C200",

  /**
   * unknown `cmd`
   */
  C201 = "C201",

  /**
   * duplicate `arg`
   */
  C300 = "C300",

  /**
   * duplicate `cmd`
   */
  C301 = "C301",

  /**
   * arg constraint `violation`
   */
  C400 = "C400"
}